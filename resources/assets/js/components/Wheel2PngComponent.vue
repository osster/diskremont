                    }

                    var idx = this.hslToRgb(fill_hsl[0], fill_hsl[1], fill_hsl[2]);

                    rendered = 'rgb(' + idx.join(',') + ')';

                    this.setCacheColor(color, this.diskFillColor, rendered);
                }
                return rendered;
            },
