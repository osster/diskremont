                        c = [c[0], c[0], c[1], c[1], c[2], c[2]];
                    }

//                    var cs = _.chunk(c, 2);
//                    console.log('c', c);
//                    return [parseInt(cs[0], 16), parseInt(cs[1], 16), parseInt(cs[2], 16)];
                    c = '0x' + c.join('');
                    return [(c >> 16) & 255, (c >> 8) & 255, c & 255];
                }
                throw new Error('Bad Hex ' + hex);
            },
